#include <iostream>
#include <vector>
#include <algorithm>
using namespace std ;

int main() {
    
    int n ;
    cin >> n ;
    

    int max_val = 0, min_val = 101  ;
    int max_idx = 0, min_idx = 0 ;

    for (int i = 0; i < n; i++) {
        int height ;
        cin >> height ;

        if (height > max_val) {
            max_val = height ;
            max_idx = i ;
        }
        
        
        if (height <= min_val) {
            min_val = height ;
            min_idx = i ;
        }
    }


    int total_swaps = max_idx + (n - 1 - min_idx) ;

    if (max_idx > min_idx) {
        total_swaps-- ;
    }

    cout << total_swaps << endl ;

    return 0;
}
